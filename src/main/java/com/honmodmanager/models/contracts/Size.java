package com.honmodmanager.models.contracts;

/**
 * Contains width and height of an element.
 *
 * @author Burgy Benjamin
 */
public interface Size
{
    /**
     * Returns the width of an element.
     *
     * @return
     */
    double getWidth();

    /**
     * Returns the height of an element.
     *
     * @return
     */
    double getHeight();
}
